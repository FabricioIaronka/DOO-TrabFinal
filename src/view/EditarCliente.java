    private void BotaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoEditarActionPerformed
        try{
            ClienteController cc = new ClienteController();

            String nome = TextoNome.getText();
            String sobrenome = TextoSobrenome.getText();
            String telefone = TextoTelefone.getText();
            String endereco = TextoEndereco.getText();
            String cpf = TextoCPF.getText();

            cc.updateProduto(this.cl.getId(), nome, sobrenome, telefone, endereco, cpf);
            telaPrincipal.updateListaClientes();

            JOptionPane.showMessageDialog(
                this,
                "Cliente editado com sucesso",
                "Sucesso!",
                JOptionPane.INFORMATION_MESSAGE
            );

            dispose();
        }
        catch (ClientInvalid e){
            JOptionPane.showMessageDialog(
                this,
                e.getMessage(),
                "Erro ao editar o cliente",
                JOptionPane.ERROR_MESSAGE
            );
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(
                this,
                e.getMessage(),
                "Erro no banco de dados",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_BotaoEditarActionPerformed



